def apply_shock_to_params(
    base_params: dict,
    shock: ShockProfile,
    onset_quarter: int,
    severity: float = 1.0,
    n_total_quarters: int = 24,
) -> List[dict]:
    """
    Generate per-quarter input series by injecting EXOGENOUS shock deltas.
    Policy variables (fedRate, govSpending, taxRate, M2) stay at baseline —
    the engine's Taylor Rule and fiscal stabilizer handle the response.
    """
    series = []
    for q in range(n_total_quarters):
        pq = base_params.copy()
        pq["q"] = f"Q{q + 1}"
        shock_q = q - onset_quarter

        if 0 <= shock_q < shock.n_quarters:
            deltas = shock.exogenous_deltas[shock_q]
            for var, delta in deltas.items():
                if var in pq:
                    new_val = pq[var] + delta * severity
                    if var == "oilPrice":
                        new_val = max(10, new_val)
                    elif var == "tariffRate":
                        new_val = max(0, new_val)
                    pq[var] = round(new_val, 3)
        elif shock_q >= shock.n_quarters:
            # Post-shock recovery: 80%/quarter exponential decay toward baseline
            recovery_factor = 0.8 ** (shock_q - shock.n_quarters + 1)
            last_deltas = shock.exogenous_deltas[-1]
            for var, delta in last_deltas.items():
                if var in pq:
                    new_val = base_params[var] + delta * severity * recovery_factor
                    if var == "oilPrice":
                        new_val = max(10, new_val)
                    elif var == "tariffRate":
                        new_val = max(0, new_val)
                    pq[var] = round(new_val, 3)

        if "debtToGDP" not in pq:
            pq["debtToGDP"] = base_params.get("debtToGDP", 124)
        series.append(pq)

    return series


