"""
Stress Test + Monte Carlo — stress.py

Crisis shock profiles EXTRACTED FROM REAL HISTORICAL DATA but split into:
- EXOGENOUS variables (oil, labor force, productivity, tariffs) — applied as the shock
- POLICY variables (fed rate, spending, tax, M2) — EXCLUDED, let engine respond endogenously

This means the shock magnitudes are historically calibrated (real data) but the
policy response is generated by the engine's Taylor Rule + fiscal stabilizers,
not baked into the shock profile.
"""

