# ═══════════════════════════════════════════════════════════════
# EXOGENOUS vs POLICY classification
# ═══════════════════════════════════════════════════════════════

# Variables that CAUSE crises (extracted from history, applied as shocks)
EXOGENOUS_VARS = ["oilPrice", "laborForceGrowth", "productivityGrowth", "tariffRate"]

# Variables that are RESPONSES to crises (excluded from shock, engine generates them)
POLICY_VARS = ["fedRate", "govSpending", "taxRate", "moneySupplyGrowth"]


@dataclass
class ShockProfile:
    name: str
    description: str
    n_quarters: int
    source: str                     # which historical crisis this was extracted from
    exogenous_deltas: List[Dict[str, float]] = field(default_factory=list)
    # Also store what the historical policy response was, for reference
    historical_policy: List[Dict[str, float]] = field(default_factory=list)


def _extract_from_history(crisis_key: str, start_idx: int, end_idx: int,
                           label: str, desc: str) -> ShockProfile:
    """
    Extract exogenous shock deltas from real historical data.

    Only EXOGENOUS_VARS are included in the shock profile.
    POLICY_VARS are stored separately for reference but NOT applied.
    """
    ds = DATASETS[crisis_key]
    inputs = ds.inputs.to_dict("records")
    baseline = inputs[start_idx]
    n = min(end_idx + 1, len(inputs))

    exo_deltas = []
    pol_deltas = []
    for q in range(start_idx, n):
        exo = {v: round(inputs[q][v] - baseline[v], 3) for v in EXOGENOUS_VARS}
        pol = {v: round(inputs[q][v] - baseline[v], 3) for v in POLICY_VARS}
        exo_deltas.append(exo)
        pol_deltas.append(pol)

    return ShockProfile(
        name=label,
        description=desc,
        n_quarters=len(exo_deltas),
        source=f"{ds.label} (Q{start_idx+1}→Q{n})",
        exogenous_deltas=exo_deltas,
        historical_policy=pol_deltas,
    )


